import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    hover?: boolean;
    interactive?: boolean;
    variant?: "default" | "glass" | "elevated" | "minimal";
  }
>(({ className, hover = false, interactive = false, variant = "default", ...props }, ref) => {
  const baseClasses = "rounded-lg border bg-card text-card-foreground shadow-sm transform-gpu";
  
  const variantClasses = {
    default: "border-border",
    glass: "glass-secondary",
    elevated: "glass-primary",
    minimal: "border-transparent shadow-none bg-transparent"
  };
  
  const interactionClasses = cn(
    hover && "hover-lift cursor-pointer",
    interactive && "interactive-focus"
  );
  
  return (
    <div
      ref={ref}
      className={cn(
        baseClasses,
        variantClasses[variant],
        interactionClasses,
        className
      )}
      {...props}
    />
  );
})
Card.displayName = "Card"

